import lombok.Data;
import lombok.NoArgsConstructor;


@Data

@NoArgsConstructor
public class Date {
    private int day;
    private int month;
    private int year;

    public void setYear(int year) {
        this.year = year;
    }

    public void setMonth(int month) {
        this.month = month;
    }

    public void setDay(int day) {
        this.day = day;
    }

    public int getYear() {
        return year;
    }

    public int getMonth() {
        return month;
    }

    public int getDay() {
        return day;
    }

    public Date(int day, int month, int year) {
        this.day = day;
        this.month = month;
        this.year = year;
    }

    @Override
    public String toString() {
        return "Birthday: " + day + "" + month + "." + year;
    }
}